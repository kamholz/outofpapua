# headword and gloss cleanup

sub ignore_if_only_char {
  my ($char) = @_;
  return sub {
    return $_[0] =~ s/^\Q${char}\E+$//r;
  }
}

sub replace_with_space {
  my ($re) = @_;
  return sub {
    return $_[0] =~ s/\Q${re}\E/ /gr;
  }
}

sub strip_paren {
  return $_[0] =~ s/ *\([^()]+\)$//r;
}

