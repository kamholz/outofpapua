    foreach my $line (split /\n/, $txt) {
      if ($line =~ m{^(.+?) *// *(.+?) *$}) {
        my ($xv, $tr) = ($1, $2);
        $tr =~ s{ *// *}{ }g;
        # $tr =~ s{.*// *}{}g;
        # $tr =~ s/^'|'$//g;
        push @examples, [$xv, [$tr, $tr_lang]];
      } else {
        warn "could not match example line: $line";
      }
    }
    return @examples;
  };
}

