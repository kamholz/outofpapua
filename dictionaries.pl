sub replace_underscore {
  return $_[0] =~ s/_/ /gr;
}

sub strip_paren {
  return $_[0] =~ s/ *\([^()]+\)$//r;
}

# example conversion

sub examples_flassy {
  my ($txt) = @_;
  my @examples;
  foreach my $line (split /\n/, $txt) {
    if ($line =~ m{^(.+?) *// *(.+?) *$}) {
      my ($xv, $xn) = ($1, $2);
      $xn =~ s{ *// *}{ }g;
      push @examples, [$xv, [$xn, 'ind']];
    } else {
      warn "could not match example line: $line";
    }
  }
  return @examples;
}

1;